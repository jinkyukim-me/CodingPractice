package sec01;

public class VariableInitializationExample {

    public static void main(String[] args) {

        // 변수 value 선언
        int value;
        // 값 저장
        value = 0;


        // 연산의 결과값을 변수 result의 초기값으로 저장
        int result = value + 10;

        System.out.println(result);
    }
}
